package ex_Loops;

public class Lab005_For_IF_Loop {
    public static void main(String[] args) {
        for (int shruti=0;shruti<18;shruti++)
        {
            if(shruti>15){
                System.out.println("She can get iPhone");
            }
            else {
                System.out.println("No gift");
            }
        }
    }
}
